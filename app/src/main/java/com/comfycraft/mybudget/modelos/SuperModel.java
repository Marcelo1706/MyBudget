package com.comfycraft.mybudget.modelos;

public class SuperModel {
    public String nombre;
    public String total;
    public String cantProds;

    public SuperModel(){
        super();
    }

    public SuperModel(String nombre, String total, String cantProds) {
        super();
        this.nombre = nombre;
        this.total = total;
        this.cantProds = cantProds;
    }
}
