package com.comfycraft.mybudget;

public class SuperModel {
    String nombre;
    String total;
    String cantProds;

    public SuperModel(){
        super();
    }

    public SuperModel(String nombre, String total, String cantProds) {
        super();
        this.nombre = nombre;
        this.total = total;
        this.cantProds = cantProds;
    }
}
